/**
 * Get local Philippine name from API/common name
 */
export function getLocalName(apiName) {
  if (!apiName) return 'Unknown';
  
  // Direct match
  if (plantNameMapping[apiName]) {
    return plantNameMapping[apiName];
  }
  
  // Case-insensitive match
  const lowerName = apiName.toLowerCase();
  for (const [key, value] of Object.entries(plantNameMapping)) {
    if (key.toLowerCase() === lowerName) {
      return value;
    }
  }
  
  // Partial match
  for (const [key, value] of Object.entries(plantNameMapping)) {
    if (lowerName.includes(key.toLowerCase()) || key.toLowerCase().includes(lowerName)) {
      return value;
    }
  }
  
  // Return original if no match
  return apiName;
}
